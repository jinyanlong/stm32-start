//保存pInBit(bit)中的数据到m_T(byte)
void BitToByte(UInt8 *pInBit){
    UInt8 i;
	for(i=0 ; i<=7 ; i++){
		if(pInBit[i] & 0x80) m_T[i*8+0] = 0x01 ;
		else m_T[i*8+0] = 0 ;
		if(pInBit[i] & 0x40) m_T[i*8+1] = 0x01 ;
		else m_T[i*8+1] = 0 ;
		if(pInBit[i] & 0x20) m_T[i*8+2] = 0x01 ;
		else m_T[i*8+2] = 0 ;
		if(pInBit[i] & 0x10) m_T[i*8+3] = 0x01 ;
		else m_T[i*8+3] = 0 ;
		if(pInBit[i] & 0x08) m_T[i*8+4] = 0x01 ;
		else m_T[i*8+4] = 0 ;
		if(pInBit[i] & 0x04) m_T[i*8+5] = 0x01 ;
		else m_T[i*8+5] = 0 ;
		if(pInBit[i] & 0x02) m_T[i*8+6] = 0x01 ;
		else m_T[i*8+6] = 0 ;
		if(pInBit[i] & 0x01) m_T[i*8+7] = 0x01 ;
		else m_T[i*8+7] = 0 ;
	}
}

//提取m_T(byte)中的数据到pOutBit(bit)
void ByteToBit(UInt8 *pOutBit){
    UInt8 i,j;
	for(i = 0;i < 8;i++){
		pOutBit[i]=0;
		for(j=0;j<8;j++){
			if(m_T[(UInt8)(i<<3)+j])
				pOutBit[i]|=(UInt8)(0x80>>j);
		}
	}
}

