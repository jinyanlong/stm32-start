void Des_E(){
	UInt8 i;
	for( i=0;i<32;i++)
		m_OldL[i]=m_B[i];      //保存L
	for(i=0;i<32;i++)
		m_B[i]=m_B[i+32];   //换位及保存R
	for(i=0;i<48;i++)
		m_J[i]=m_B[32+m_E[i]-1];//计算J
}

//入口：m_J[0-47]中存放E(R(i))
//出口：m_J[0-47]中存放E(R(i))^K(i+1)
void Des_XR(UInt8* pKeyBit){
    UInt8 i;
	for( i=0;i<48;i++){
		m_J[i]^=pKeyBit[i];
	}
}

