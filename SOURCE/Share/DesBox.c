	//bDirect=TRUE 为加密 ，bDirect=FALSE 为解密
void Des_Crypt(UInt8 *pOut,UInt8 *pIn, DESKEYITEM* pKeyGroup,bool bDirect){
    UInt8 i;
	BitToByte(pIn);//pIn->T
	Des_IP();//T->L,R

	for( i=0;i<16;i++){
		Des_E();//E变换 R->J,R->L,保存L->m_OldL
		if(bDirect)
			Des_XR(pKeyGroup[i]);//加密 J=J^K
		else
			Des_XR(pKeyGroup[15-i]);//解密 J=J^K

		Des_S();//S算子 J->tpR->T
		Des_P();//P变换 T->J
		Des_XT(i);//J->L,R
	}

	Des_UNIP();//L,R->T
	ByteToBit(pOut);//T->pOut
}


