//入口：m_B[0-31],m_B[32-63]中存放R(i),m_J[0-31]中存放f(R,K)
//出口：
//Order!=15: m_B[0-31]中存放R(i),m_B[32-63]中存放R(i+1)
//Order==15: m_B[0-31]中存放R(i+1),m_B[32-63]中存放R(i)
void Des_XT(UInt8 Order){
    UInt8 i;
	if(Order==15){
		for(i=0;i<32;i++)
			m_B[i]=m_OldL[i]^m_J[i];
		//m_B[32+i]存放R(i)
	}
	else{
		//m_B[32+i]存放R(i)
		for( i=0;i<32;i++)
			m_B[32+i]=m_OldL[i]^m_J[i];
	}
}

