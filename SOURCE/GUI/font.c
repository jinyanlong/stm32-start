UInt8 FONT_WIDTH(UInt8 const * pFontLib,UInt8 nMinWidth,UInt8 nMaxWidth){
    UInt8 nWidth=nMaxWidth;
    while( (nWidth>=nMinWidth) && (pFontLib[nWidth-1]==0) && (pFontLib[nMaxWidth+nWidth-1]==0) ){
        nWidth--;
    }
    return (nWidth<nMaxWidth)?(nWidth+1):nMaxWidth;
}

